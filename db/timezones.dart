  static Timezone getTimezoneByAbbreviation(
    String abbrevation,
    String zonename,
  ) {
    return timezones.firstWhere(
      (element) =>
          element.abbrevation == abbrevation && element.zonename == zonename,
    );
  }

  static List<Timezone> getTimezonesByAbbreviation(String abbrevation) {
    return timezones
        .where(
          (element) => element.abbrevation == abbrevation,
        )
        .toList();
  }

  static List<Timezone> getZones({
    String? location,
    String? country,
  }) {
    if (location != null && country != null) {
      return timezones
          .where(
            (element) =>
                element.location!.toLowerCase() == location.toLowerCase() &&
                element.zonename!.toLowerCase().contains(country.toLowerCase()),
          )
          .toList();
    }
    if (location != null) {
      return timezones
          .where(
            (element) =>
                element.location!.toLowerCase() == location.toLowerCase(),
          )
          .toList();
    }

    if (country != null) {
      return timezones
          .where(
            (element) =>
                element.zonename!.toLowerCase().contains(country.toLowerCase()),
          )
          .toList();
    }
    return timezones;
  }
